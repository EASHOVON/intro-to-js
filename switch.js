var color = 'red';
switch (color)
{
    case 'red':
        console.log('This is red color');
        break;
    case 'green':
        console.log("This is green color");
        break;
    case 'blue':
        console.log("This is blue color");
        break;
    default:
        console.log("Sorry your color does not matched");
}